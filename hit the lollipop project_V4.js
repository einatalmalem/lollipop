		function calculateTime() { //חישוב זמן
			var min, sec;
			if (totalTime > 60) {
				min = parseInt(totalTime / 60);
				if (min < 10)
					min = "0" + min.toString();
				sec = totalTime % 60;
				if (sec < 10)
					sec = "0" + sec.toString();
			} else {
				min = "00"
				if (totalTime < 10) {
					sec = "0" + totalTime.toString();
				} else {
					sec = totalTime.toString();
				}
			}
			return min + ":" + sec;
		}
		
